    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        String idPatient = jTextField3.getText();
        String date = jComboBox1.getSelectedItem().toString();
        String idMaladie;
        String idIntervention;

        if (jRadioButton1.isSelected()) {
            idIntervention = "XX1";
        } else if (jRadioButton2.isSelected()) {
            idIntervention = "XX2";
        } else {
            idIntervention = "XX3";
        }

        if (jRadioButton4.isSelected()) {
            idMaladie = "MM1";
        } else if (jRadioButton5.isSelected()) {
            idMaladie = "MM2";
        } else if (jRadioButton6.isSelected()) {
            idMaladie = "MM3";
        } else {
            idMaladie = "MM4";
        }

        int resultat = is.creerIntervention(id, idIntervention, idPatient, idMaladie, date, interventions);

        if (resultat == -1) {
            JOptionPane.showMessageDialog(null, "intervention existe d√©j√†", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "cr√©ation avec succ√®s", "Info", JOptionPane.INFORMATION_MESSAGE);
            jComboBox1.removeItem(date);
            jComboBox2.addItem(idMaladie);

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

